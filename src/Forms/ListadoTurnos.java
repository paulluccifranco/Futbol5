    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String formato = "yyyyMMdd";
        java.util.Date dsd = DateDesde.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat(formato);
        desde = String.valueOf(sdf.format(dsd));

        java.util.Date hast = DateHasta.getDate();
        hasta = String.valueOf(sdf.format(hast));
        
        String telefono = "";
        String nombre = "";
        Integer asistencias = 0;
        Integer faltas = 0;
        
        if(!txtNombre.getText().equals("")){
            nombre = " AND turno = '"+txtNombre.getText()+"'";
        }
        if(!txtTelefono.getText().equals("")){
            telefono = " AND telefono = '"+txtTelefono.getText()+"'";
        }
        
        if(!txtAsistencias.getText().equals("")){
            asistencias = Integer.parseInt(txtAsistencias.getText());
        }
        
        if(!txtFaltas.getText().equals("")){
            faltas = Integer.parseInt(txtFaltas.getText());
        }

        v.visualizar_listaturno(TablaTurnos, desde, hasta, nombre, telefono, asistencias, faltas, txtTotal);
    }//GEN-LAST:event_jButton1ActionPerformed


